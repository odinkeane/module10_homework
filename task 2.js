let value2 = ["124asdf"];
if (typeof value2 == "boolean"){
    console.log(`${value2} - логический тип`);
} else if (typeof value2 == "string"){
    console.log(`${value2} - строка`);
} else if (typeof value2 == "number"){
    console.log(`${value2} - число`);
} else{ 
    console.log(`Не определен`);
}